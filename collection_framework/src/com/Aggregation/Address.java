package com.Aggregation;
//no inheritance use
//has-a relationship
//code reuse
public class Address {

	 int adressid;
	 String landmark;
	String city;
	 String pincode;
	 
	public Address(int adressid, String landmark, String city, String pincode) {
		super();
		this.adressid = adressid;
		this.landmark = landmark;
		this.city = city;
		this.pincode = pincode;
	}
	
	
	
}
